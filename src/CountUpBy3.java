public class CountUpBy3 {
    public static void main(String[] args) {
        // Pseudocode:
// START
//   Create a loop starting at 0 and ending at 18
//   Increase the counter by 3 each time
//   Display each number with a space
// STOP
        for (int i = 0; i <= 18; i += 3) {
            System.out.print(i + " ");
        }
    }
}


