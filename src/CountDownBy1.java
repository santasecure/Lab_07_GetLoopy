public class CountDownBy1 {
    public static void main(String[] args) {
        // Pseudocode:
// START
//   Create a loop from 30 to 0, stepping down by 1
//   For each number, display it with a space
// STOP
        for (int i = 30; i >= 0; i--) {
            System.out.print(i + " ");
        }
    }
}

