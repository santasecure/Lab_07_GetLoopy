public class CountUpBy1 {
    public static void main(String[] args) {
        // Pseudocode:
// START
//   Create a loop from 0 to 30, stepping by 1
//   For each number, display it with a space
// STOP
        for (int i = 0; i <= 30; i++) {
            System.out.print(i + " ");
        }
    }
}


